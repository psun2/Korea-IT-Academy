package control_p;

public class WhileMain {

	public static void main(String[] args) {
		
		int i=0;  			//초기값
		while(i<10) {		//조건
			
			System.out.println("while 문이다 "+i);
			
			i++;			//증감
		}
		
		//1-> 100 합
		
		int tot=0;
		i=1;  			//초기값
		while(i<=100) {		//조건
			
			tot+=i;
			
			i++;			//증감
		}
		
		System.out.println(tot);
		
		
		int a = 10, b= 30;
		
		System.out.println(a+","+b);
		
//		a = b;
//		b = a;
		
		int buf = a;
		a = b;
		b = buf;
		
		
		System.out.println(a+","+b);

	}

}
